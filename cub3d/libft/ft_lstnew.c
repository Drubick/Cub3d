#include "libft.h"

t_list	*ft_lstnew(void const *content)
{
	t_list	*new_elem;

	new_elem = (t_list *)malloc(sizeof(t_list));
	if (!new_elem)
		return (NULL);
	new_elem->content = (void *)content;
	new_elem->next = NULL;
	return (new_elem);
}
